import { copyFile, mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const sourceDir = path.join(root, "src", "embeds");
const distDir = path.join(root, "dist");
const publicEmbedDir = path.join(root, "public", "embeds");
const themeAssetDir = path.join(root, "wordpress-theme", "aurum-video", "assets", "player");

const jsSource = await readFile(path.join(sourceDir, "aurum-video-player.js"), "utf8");
const cssSource = await readFile(path.join(sourceDir, "aurum-video-player.css"), "utf8");
const hlsSource = await readFile(path.join(root, "node_modules", "hls.js", "dist", "hls.light.min.js"), "utf8");

const banner = "/* AURUM Video Player - generated by scripts/build-aurum-player.mjs */\n";
const jsOutput = [
  banner,
  hlsSource,
  "\n;",
  jsSource.replace('"__AURUM_PLAYER_CSS__"', JSON.stringify(cssSource)),
  "\n",
].join("");

await mkdir(distDir, { recursive: true });
await mkdir(publicEmbedDir, { recursive: true });
await mkdir(themeAssetDir, { recursive: true });
await writeFile(path.join(distDir, "aurum-video-player.js"), jsOutput, "utf8");
await writeFile(path.join(distDir, "aurum-video-player.css"), banner + cssSource, "utf8");
await copyFile(path.join(distDir, "aurum-video-player.js"), path.join(publicEmbedDir, "aurum-video-player.js"));
await copyFile(path.join(distDir, "aurum-video-player.css"), path.join(publicEmbedDir, "aurum-video-player.css"));
await copyFile(path.join(distDir, "aurum-video-player.js"), path.join(themeAssetDir, "aurum-video-player.js"));
await copyFile(path.join(distDir, "aurum-video-player.css"), path.join(themeAssetDir, "aurum-video-player.css"));

console.log("Built dist/aurum-video-player.js");
console.log("Built dist/aurum-video-player.css");
console.log("Copied player assets to public/embeds");
console.log("Copied player assets to wordpress-theme/aurum-video/assets/player");
